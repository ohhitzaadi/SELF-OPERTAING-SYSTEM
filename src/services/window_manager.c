#define CURSOR_W 8
#define CURSOR_H 12

static const unsigned char mouse_cursor_sprite[12][8] = {
    {0, 255, 255, 255, 255, 255, 255, 255},
    {0, 0, 255, 255, 255, 255, 255, 255},
    {0, 15, 0, 255, 255, 255, 255, 255},
    {0, 15, 15, 0, 255, 255, 255, 255},
    {0, 15, 15, 15, 0, 255, 255, 255},
    {0, 15, 15, 15, 15, 0, 255, 255},
    {0, 15, 15, 15, 15, 15, 0, 255},
    {0, 15, 15, 15, 0, 0, 0, 0},
    {0, 15, 0, 15, 15, 0, 255, 255},
    {0, 0, 255, 0, 15, 15, 0, 255},
    {255, 255, 255, 255, 0, 0, 255, 255},
    {255, 255, 255, 255, 255, 255, 255, 255}
};

static void draw_mouse_cursor(unsigned char *buf, int mx, int my) {
  for (int y = 0; y < CURSOR_H; y++) {
    for (int x = 0; x < CURSOR_W; x++) {
      if (mx + x >= 0 && mx + x < 320 && my + y >= 0 && my + y < 200) {
        unsigned char color = mouse_cursor_sprite[y][x];
        if (color != 255) {
          buf[(my + y) * 320 + (mx + x)] = color;
        }
      }
    }
  }
}

void wm_clear(unsigned char color) {
  for (int i = 0; i < 320 * 200; i++) {
    backbuffer[i] = color;
  }
}

void wm_draw_pixel(int x, int y, unsigned char color) {
  if (x >= 0 && x < 320 && y >= 0 && y < 200) {
    backbuffer[y * 320 + x] = color;
  }
}

void wm_swap() {
  unsigned char *vga = (unsigned char *)0xA0000;
  for (int i = 0; i < 320 * 200; i++) {
    vga[i] = vga[i]; // prevent optimization
    vga[i] = backbuffer[i];
  }
  draw_mouse_cursor(vga, mouse_x, mouse_y);
}

void wm_draw_char(int x, int y, char c, unsigned char color) {
  if (c < 32 || c > 126) {
    c = ' ';
  }
  int idx = c - 32;
  for (int col = 0; col < 5; col++) {
    unsigned char col_bits = font5x7[idx][col];
    for (int row = 0; row < 7; row++) {
      if (col_bits & (1 << row)) {
        wm_draw_pixel(x + col, y + row, color);
      }
    }
  }
}

void wm_draw_string(int x, int y, const char *str, unsigned char color) {
  int cur_x = x;
  for (int i = 0; str[i] != '\0'; i++) {
    if (str[i] == '\n') {
      y += 8;
      cur_x = x;
    } else {
      wm_draw_char(cur_x, y, str[i], color);
      cur_x += 6;
    }
  }
}

void wm_draw_line(int x1, int y1, int x2, int y2, unsigned char color) {
  int dx = x1 < x2 ? x2 - x1 : x1 - x2;
  int dy = y1 < y2 ? y2 - y1 : y1 - y2;
  int sx = x1 < x2 ? 1 : -1;
  int sy = y1 < y2 ? 1 : -1;
  int err = (dx > dy ? dx : -dy) / 2;
  int e2;

  while (1) {
    wm_draw_pixel(x1, y1, color);
    if (x1 == x2 && y1 == y2)
      break;
    e2 = err;
    if (e2 > -dx) {
      err -= dy;
      x1 += sx;
    }
    if (e2 < dy) {
      err += dx;
      y1 += sy;
    }
  }
}

void wm_draw_circle(int xc, int yc, int r, unsigned char color) {
  int x = 0;
  int y = r;
  int d = 3 - 2 * r;

  while (y >= x) {
    wm_draw_pixel(xc + x, yc + y, color);
    wm_draw_pixel(xc - x, yc + y, color);
    wm_draw_pixel(xc + x, yc - y, color);
    wm_draw_pixel(xc - x, yc - y, color);
    wm_draw_pixel(xc + y, yc + x, color);
    wm_draw_pixel(xc - y, yc + x, color);
    wm_draw_pixel(xc + y, yc - x, color);
    wm_draw_pixel(xc - y, yc - x, color);
    x++;
    if (d > 0) {
      y--;
      d = d + 4 * (x - y) + 10;
    } else {
      d = d + 4 * x + 6;
    }
  }
}

void wm_draw_rect(int x, int y, int w, int h, unsigned char color) {
  for (int i = 0; i < w; i++) {
    wm_draw_pixel(x + i, y, color);
    wm_draw_pixel(x + i, y + h - 1, color);
  }
  for (int i = 0; i < h; i++) {
    wm_draw_pixel(x, y + i, color);
    wm_draw_pixel(x + w - 1, y + i, color);
  }
}

void wm_draw_rect_filled(int x, int y, int w, int h, unsigned char color) {
  for (int r = 0; r < h; r++) {
    for (int c = 0; c < w; c++) {
      wm_draw_pixel(x + c, y + r, color);
    }
  }
}
